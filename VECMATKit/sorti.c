void VECMATMatrix4ProjectionOrtho(float left, float right, float bottom, float top, float nearZ, float farZ, VECMATMatrix4 projection)
{
    float r_l = right - left;
    float t_b = top - bottom;
    float f_n = farZ - nearZ;


    projection[0][0] = 2 / r_l;
    projection[0][1] = 0;
    projection[0][2] = 0;
    projection[0][3] = 0;

    projection[1][0] = 0;
    projection[1][1] = 2 / t_b;
    projection[1][2] = 0;
    projection[1][3] = 0;

    projection[2][0] = 0;
    projection[2][1] = 0;
    projection[2][2] = -2 / (f_n);
    projection[2][3] = 0;

    projection[3][0] = (-right - left) / r_l;
    projection[3][1] = (-top - bottom) / t_b;
    projection[3][2] = (-farZ - nearZ) / f_n;
    projection[3][3] = 1;
}






void VECMATMatrix4ProjectionPerspective(float ratio, float angle, float Znear, float Zfar, VECMATMatrix4 projection)
{
    float tanedAngle;
    float rad_angle = (angle * 3.14159265358979323846264338327950288 / 180);

    tanedAngle = tan(rad_angle / 2);

    projection[0][0] = 1.0f / (ratio * tanedAngle);
    projection[1][0] = 0.0f;
    projection[2][0] = 0.0f;
    projection[3][0] = 0.0f;

    projection[0][1] = 0.0f;
    projection[1][1] = 1 / tanedAngle;
    projection[2][1] = 0.0f;
    projection[3][1] = 0.0f;

    projection[0][2] = 0.0f;
    projection[1][2] = 0.0f;
    projection[2][2] = (Znear + Zfar) / (Znear - Zfar);
    projection[3][2] = (2 * Zfar * Znear) / (Znear - Zfar);

    projection[0][3] = 0.0f;
    projection[1][3] = 0.0f;
    projection[2][3] = -1.0f;
    projection[3][3] = 0.0f;
}




void VECMATMatrix4ProjectionFrustum(float left, float right, float bottom, float top, float nearZ, float farZ, VECMATMatrix4 projection)
{
    float _2xN = 2 * nearZ;
    float r_l = right - left;
    float t_b = top - bottom;
    float n_f = nearZ - farZ;


    projection[0][0] = _2xN / r_l;
    projection[0][1] = 0;
    projection[0][2] = 0;
    projection[0][3] = 0;

    projection[1][0] = 0;
    projection[1][1] = _2xN / t_b;
    projection[1][2] = 0;
    projection[1][3] = 0;

    projection[2][0] = (right + left) / r_l;
    projection[2][1] = (top + bottom) / t_b;
    projection[2][2] = (farZ + nearZ) / n_f;
    projection[2][3] = -1;

    projection[3][0] = 0;
    projection[3][1] = 0;
    projection[3][2] = (_2xN * farZ)/ n_f;
    projection[3][3] = 0;
}







void VECMATMatrix4LookAt(float Cx, float Cy, float Cz, float Tx, float Ty, float Tz, float Ux, float Uy, float Uz, VECMATMatrix4 look)
{
    VECMATVector3 f;
    VECMATVector3 l;
    VECMATVector3 u;

    VECMATVector3 up = {
        Ux, Uy, Uz};

    VECMATVector3CreateNormalized(Tx, Ty, Tz, Cx, Cy, Cz, f);
    VECMATVector3CrossProduct(up, f, l);
    VECMATVector3Normalize(l);
    VECMATVector3CrossProduct(f, l, u);

    look[0][0] = l[0];
    look[1][0] = l[1];
    look[2][0] = l[2];
    look[3][0] = -l[0] * Cx - l[1] * Cy - l[2] * Cz;

    look[0][1] = u[0];
    look[1][1] = u[1];
    look[2][1] = u[2];
    look[3][1] = -u[0] * Cx - u[1] * Cy - u[2] * Cz;

    look[0][2] = f[0];
    look[1][2] = f[1];
    look[2][2] = f[2];
    look[3][2] = -f[0] * Cx - f[1] * Cy - f[2] * Cz;

    look[0][3] = 0;
    look[1][3] = 0;
    look[2][3] = 0;
    look[3][3] = 1;
}
